package com.pojo;

/**
 * @function 保存用户信息的一个类
 * @author 王飞
 * @date 2018-11-28
 */
public class UserInfo {
	private Integer username ;
	private String user_realname;
	private String user_born_date;
	private String user_location;
	private String user_qianming ;
	private String user_level;
	private String user_date;
	private String user_img;
	
	public Integer getUsername() {
		return username;
	}
	public void setUsername(Integer username) {
		this.username = username;
	}
	public String getUser_realname() {
		return user_realname;
	}
	public void setUser_realname(String user_realname) {
		this.user_realname = user_realname;
	}
	public String getUser_born_date() {
		return user_born_date;
	}
	public void setUser_born_date(String user_born_date) {
		this.user_born_date = user_born_date;
	}
	public String getUser_location() {
		return user_location;
	}
	public void setUser_location(String user_location) {
		this.user_location = user_location;
	}
	public String getUser_qianming() {
		return user_qianming;
	}
	public void setUser_qianming(String user_qianming) {
		this.user_qianming = user_qianming;
	}
	public String getUser_level() {
		return user_level;
	}
	public void setUser_level(String user_level) {
		this.user_level = user_level;
	}
	public String getUser_date() {
		return user_date;
	}
	public void setUser_date(String user_date) {
		this.user_date = user_date;
	}
	public String getUser_img() {
		return user_img;
	}
	public void setUser_img(String user_img) {
		this.user_img = user_img;
	}
}
